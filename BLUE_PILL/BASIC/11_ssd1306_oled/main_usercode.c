/* ===== USER CODE BEGIN 0 (Private function prototypes & implementations) ===== */

/* Declared here; hi2c1 is defined in the generated main.c */
extern I2C_HandleTypeDef hi2c1;

/**
 * @brief Send a single command byte to the SSD1306.
 */
static HAL_StatusTypeDef SSD1306_SendCmd(uint8_t cmd)
{
    uint8_t buf[2] = { SSD1306_CMD_BYTE, cmd };
    return HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR,
                                   buf, 2, I2C_TIMEOUT_MS);
}

/**
 * @brief Send the SSD1306 initialisation sequence.
 */
static HAL_StatusTypeDef SSD1306_Init(void)
{
    HAL_StatusTypeDef ret;
    HAL_Delay(100); /* power-on delay */

    const uint8_t init_seq[] = {
        0xAE,       /* display off */
        0xD5, 0x80, /* clock divide / oscillator frequency */
        0xA8, 0x3F, /* multiplex ratio 64 */
        0xD3, 0x00, /* display offset 0 */
        0x40,       /* start line 0 */
        0x8D, 0x14, /* charge pump enable */
        0x20, 0x00, /* horizontal addressing mode */
        0xA1,       /* segment re-map (mirror X) */
        0xC8,       /* COM scan direction remapped */
        0xDA, 0x12, /* COM pins hardware config */
        0x81, 0xCF, /* contrast */
        0xD9, 0xF1, /* pre-charge period */
        0xDB, 0x40, /* VCOMH deselect level */
        0xA4,       /* entire display on (follow RAM) */
        0xA6,       /* normal display (not inverted) */
        0xAF,       /* display on */
    };

    for (uint8_t i = 0; i < sizeof(init_seq); i++) {
        ret = SSD1306_SendCmd(init_seq[i]);
        if (ret != HAL_OK) return ret;
    }
    return HAL_OK;
}

/**
 * @brief Set column and page (row) address window for subsequent data writes.
 */
static HAL_StatusTypeDef SSD1306_SetCursor(uint8_t col, uint8_t page)
{
    HAL_StatusTypeDef ret;
    ret = SSD1306_SendCmd(0x21);          /* set column address */
    if (ret != HAL_OK) return ret;
    ret = SSD1306_SendCmd(col);           /* start column */
    if (ret != HAL_OK) return ret;
    ret = SSD1306_SendCmd(SSD1306_WIDTH - 1); /* end column */
    if (ret != HAL_OK) return ret;
    ret = SSD1306_SendCmd(0x22);          /* set page address */
    if (ret != HAL_OK) return ret;
    ret = SSD1306_SendCmd(page);          /* start page */
    if (ret != HAL_OK) return ret;
    ret = SSD1306_SendCmd(SSD1306_PAGES - 1); /* end page */
    return ret;
}

/**
 * @brief Flush the local frame buffer to the OLED over I2C.
 *
 * The SSD1306 data transfer requires a control byte (0x40) before each I2C
 * transaction.  We send one page at a time to keep the I2C buffer small.
 */
static HAL_StatusTypeDef SSD1306_Flush(void)
{
    HAL_StatusTypeDef ret;
    /* Temporary buffer: 1 control byte + 128 data bytes */
    uint8_t tx[SSD1306_WIDTH + 1];
    tx[0] = SSD1306_DATA_BYTE;

    ret = SSD1306_SetCursor(0, 0);
    if (ret != HAL_OK) return ret;

    for (uint8_t page = 0; page < SSD1306_PAGES; page++) {
        memcpy(&tx[1], ssd1306_buf[page], SSD1306_WIDTH);
        ret = HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR,
                                      tx, sizeof(tx), I2C_TIMEOUT_MS);
        if (ret != HAL_OK) return ret;
    }
    return HAL_OK;
}

/**
 * @brief Clear the frame buffer (all pixels off).
 */
static void SSD1306_Clear(void)
{
    memset(ssd1306_buf, 0x00, sizeof(ssd1306_buf));
}

/**
 * @brief Draw one ASCII character at the given column (pixel) and page (0–7).
 *        Each character occupies 6 columns (5 glyph + 1 gap).
 *        Characters outside the printable range are replaced with a space.
 */
static void SSD1306_DrawChar(uint8_t col, uint8_t page, char ch)
{
    if (ch < 0x20 || ch > 0x7E) ch = 0x20;
    const uint8_t *glyph = font5x7[(uint8_t)(ch - 0x20)];

    for (uint8_t i = 0; i < 5; i++) {
        if ((col + i) < SSD1306_WIDTH) {
            ssd1306_buf[page][col + i] = glyph[i];
        }
    }
    /* 1-pixel gap between characters */
    if ((col + 5) < SSD1306_WIDTH) {
        ssd1306_buf[page][col + 5] = 0x00;
    }
}

/**
 * @brief Draw a null-terminated string starting at (col, page).
 *        Wrapping is not handled — the string is clipped at the right edge.
 */
static void SSD1306_DrawString(uint8_t col, uint8_t page, const char *str)
{
    while (*str) {
        SSD1306_DrawChar(col, page, *str++);
        col += 6;
        if (col >= SSD1306_WIDTH) break;
    }
}

