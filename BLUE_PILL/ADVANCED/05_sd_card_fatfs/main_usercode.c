/**
 * @brief Error handler for FatFs errors — blinks LED rapidly and halts.
 * @param context  Short string identifying the failing call.
 * @param fr       FatFs result code.
 */
static void Error_Handler_SD(const char *context, FRESULT fr)
{
    char msg[80];
    snprintf(msg, sizeof(msg), "[ERR] %s failed: FRESULT=%d\r\n", context, (int)fr);
    UART_Print(msg);

    /* Blink onboard LED (PC13, active-low) rapidly to indicate error */
    while (1)
    {
        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET); /* LED ON  */
        HAL_Delay(100);
        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);   /* LED OFF */
        HAL_Delay(100);
    }
}

/**
 * @brief Blocking UART1 transmit helper.
 */
static void UART_Print(const char *msg)
{
    /* huart1 declared extern in main.h by CubeMX */
    HAL_UART_Transmit(&huart1, (const uint8_t *)msg, (uint16_t)strlen(msg), 200);
}

